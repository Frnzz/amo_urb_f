            <h3>1.C. ¿Qué es WCAG? ¿Por qué es tan importante validar en XHTML, CSS y WCAG 2.1 AAA?.</h3>
            <p>
                Los documentos denominados Pautas de Accesibilidad al Contenido en la Web <b>(WCAG)</b> <br>
                explican cómo hacer que el contenido Web sea accesible para personas con discapacidad. <br>
                El término "contenido" Web normalmente hace referencia a la información contenida en una página Web <br>
                o en una aplicación Web, incluyendo texto, imágenes, formularios, sonido, etc. <br>
                <br>
                <b>Permite validar</b> de acuerdo a la EN 139803:2012 / WCAG 2.0 o la EN 301549:2019 / WCAG 2.1. <br>
                La validación puede ser por código o URL, de 1 a 51 páginas que, o bien seleccionas tú, <br>
                o bien dejas que el validador seleccione por ti una muestra representativa. Valida también enlaces rotos. <br>


            </p>

