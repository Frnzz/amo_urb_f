            <p>               
                <b>Full stack:</b> <br>
                <br>
                El Full Stack es el profesional que domina las dos partes de un desarrollo web; <br>
                tanto los aspectos del front-end, la parte visual con la que el usuario puede interactuar, <br>
                como los del back-end, la gestión interna de la página y de la comunicación entre el navegador y la base de datos. <br>
                Además, de controlar los diferentes sistemas operativos y lenguajes de programación, bases de datos y servidores. <br>
                <br>
                <b>Front End:</b> <br>
                <br>
                Es el profesional encargado de traducir los diseños; los elementos gráficos, las tipografías, los colores, la estructura, <br>
                entre otros al lenguaje de programación con la finalidad de obtener una estructura que: <br>
                <br>
                •	Asegure la accesibilidad e intuición en la navegación de la web.<br>
                •	Garantice la legibilidad.<br>
                •	Satisfaga la experiencia del usuario.<br>
                <br>
                <b>Back End:</b> <br>
                <br>
                Alude a aquella parte de la programación que no es visible para el usuario. <br>
                Esta parte no se ejecuta en el navegador o dispositivo del cliente, sino en un servidor (físico o en la nube).<br>
            </p>

